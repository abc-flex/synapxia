-- Table modules
CREATE TABLE modules (
    code        VARCHAR(50)  NOT NULL,
    name        VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    sort_order  INT          NOT NULL DEFAULT 0,
    icon        VARCHAR(500),
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_modules PRIMARY KEY (code)
);

-- Table business_units
CREATE TABLE business_units (
    code        VARCHAR(50)  NOT NULL,
    name        VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    type        VARCHAR(100), -- references List_items where list='BIZ_UNIT_TYPE'
    parent      VARCHAR(50),
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_units PRIMARY KEY (code)
);

-- Table lists
CREATE TABLE lists (
    code        VARCHAR(50)  NOT NULL,
    name        VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    module      VARCHAR(50),
    type        VARCHAR(100) NOT NULL, -- references List_items where list='LIST_TYPE'
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_lists PRIMARY KEY (code)
);

-- Table list_items
CREATE TABLE list_items (
    list        VARCHAR(50)  NOT NULL,
    value       VARCHAR(100) NOT NULL,
    label       VARCHAR(200) NOT NULL,
    sort_order  INT          NOT NULL DEFAULT 0,
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_list_items PRIMARY KEY (list, value)
);

-- Table roles
CREATE TABLE roles (
    code        VARCHAR(50)  NOT NULL,
    name        VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_roles PRIMARY KEY (code)
);

-- Table users
CREATE TABLE users (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY,
    username       VARCHAR(50)   NOT NULL UNIQUE,
    email          VARCHAR(100)  NOT NULL UNIQUE,
    password_hash  VARCHAR(500)  NOT NULL,
    first_name     VARCHAR(100)  NOT NULL,
    last_name      VARCHAR(100)  NOT NULL,
    menu_role      VARCHAR(50)   NOT NULL,
    business_unit  VARCHAR(50)   NOT NULL,
    is_active      BOOLEAN       NOT NULL DEFAULT TRUE,
    created_at     TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at     TIMESTAMPTZ,
    last_login_at  TIMESTAMPTZ,
    CONSTRAINT pk_users PRIMARY KEY (id)
);

-- Table options
CREATE TABLE options (
    module      VARCHAR(50)  NOT NULL,
    code        VARCHAR(50)  NOT NULL,
    name        VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    path        VARCHAR(500),
    icon        VARCHAR(500),
	type        VARCHAR(100) NOT NULL, -- references List_items where list='OPTION_TYPE'
    sort_order  INT          NOT NULL DEFAULT 0,
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_options PRIMARY KEY (module, code)
);

-- Table privileges
CREATE TABLE privileges (
    role        VARCHAR(50)  NOT NULL,
    module      VARCHAR(50)  NOT NULL,
    option      VARCHAR(50)  NOT NULL,
    can_edit    BOOLEAN      NOT NULL DEFAULT TRUE,
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_privileges PRIMARY KEY (role, module, option)
);

-- ****************************************
-- ************ Foreign Keys **************
-- ****************************************

-- lists.module → modules.code
ALTER TABLE lists
    ADD CONSTRAINT fk_lists_modules
    FOREIGN KEY (module)
    REFERENCES modules (code);

-- business_units.parent → business_units.code
ALTER TABLE business_units
    ADD CONSTRAINT fk_parent_business_units
    FOREIGN KEY (parent)
    REFERENCES business_units (code);

-- list_items.list → lists.code
ALTER TABLE list_items
    ADD CONSTRAINT fk_list_items_lists
    FOREIGN KEY (list)
    REFERENCES lists (code);

-- users.role → roles.code
ALTER TABLE users
    ADD CONSTRAINT fk_users_roles
    FOREIGN KEY (menu_role)
    REFERENCES roles (code);

-- users.business_unit → business_units.code
ALTER TABLE users
    ADD CONSTRAINT fk_users_business_units
    FOREIGN KEY (business_unit)
    REFERENCES business_units (code);

-- options.module → modules.code
ALTER TABLE options
    ADD CONSTRAINT fk_options_modules
    FOREIGN KEY (module)
    REFERENCES modules (code);

-- privileges.role → roles.code
ALTER TABLE privileges
    ADD CONSTRAINT fk_privileges_roles
    FOREIGN KEY (role)
    REFERENCES roles (code);

-- privileges.option → options.code
ALTER TABLE privileges
    ADD CONSTRAINT fk_privileges_options
    FOREIGN KEY (module, option)
    REFERENCES options (module, code);

