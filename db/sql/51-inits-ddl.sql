-- **********************************
-- ******* Tables Initiatives *******
-- **********************************

-- Table initiatives
CREATE TABLE initiatives (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY,
    name             VARCHAR(100)  NOT NULL,
    description      VARCHAR(500),
	expected_impact  VARCHAR(100)  NOT NULL, -- references List_items.value where list='EXPECTED_IMPACT'
    priority_level   VARCHAR(100)  NOT NULL, -- references List_items.value where list='PRIORITY_LEVEL'
    reference        TEXT,
    status	         VARCHAR(100)  NOT NULL, -- references List_items.value where list='INITIATIVE_STATUS'
	tags             JSONB,
	detail           TEXT,
    score            SMALLINT,
    is_active        BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at       TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMPTZ,
    CONSTRAINT pk_initiatives PRIMARY KEY (id)
);

-- Table criterias
CREATE TABLE criterias (
    code        VARCHAR(50)   NOT NULL,
    name        VARCHAR(100)  NOT NULL,
    description VARCHAR(500),
    list        VARCHAR(50),  -- references Lists.code where type='CRITERIA'
    is_active   BOOLEAN       NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_criterias PRIMARY KEY (code)
);

-- Table diagnostics
CREATE TABLE diagnostics (
    init            BIGINT        NOT NULL,
    criteria        VARCHAR(50)   NOT NULL,
    creator_score   SMALLINT      NOT NULL, -- references List_items.value where list IN [select code from lists where type='CRITERIA']
    reviewer_score  SMALLINT,               -- references List_items.value where list IN [select code from lists where type='CRITERIA']
    rationale       TEXT,
    is_active       BOOLEAN       NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ,
    CONSTRAINT pk_diagnostics PRIMARY KEY (init, criteria)
);

-- Table favorite_inits
CREATE TABLE favorite_inits (
    user_id     BIGINT       NOT NULL,
    init        BIGINT       NOT NULL,
    is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_favorite_inits PRIMARY KEY (user_id, init)
);

-- Table collaborations
CREATE TABLE collaborations (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY,
    init           BIGINT        NOT NULL,
    user_id        BIGINT        NOT NULL,
	type	       VARCHAR(100)  NOT NULL, -- references List_items.value where list='COLLAB_TYPE'
    content        TEXT,
    reference      TEXT,
    parent         BIGINT,
	detail         TEXT,
    is_active      BOOLEAN       NOT NULL DEFAULT TRUE,
    created_at     TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at     TIMESTAMPTZ,
    CONSTRAINT pk_collaborations PRIMARY KEY (id)
);

-- Table related_inits
CREATE TABLE related_inits (
    source      BIGINT        NOT NULL,
    target      BIGINT        NOT NULL,
    type        VARCHAR(100)  NOT NULL, -- references List_items.value where list='RELATION_TYPE'
    rationale   TEXT,
    is_active   BOOLEAN       NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ,
    CONSTRAINT pk_related_inits PRIMARY KEY (source, target) 
);

-- Table init_permissions
CREATE TABLE init_permissions (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY,
    init          BIGINT        NOT NULL,
    target_type   VARCHAR(100)  NOT NULL, -- references List_items.value where list='TARGET_TYPE'
    target_code   VARCHAR(50)   NOT NULL, -- the recipient identifier
    access_level  VARCHAR(100)  NOT NULL, -- references List_items.value where list='ACCESS_LEVEL'
    valid_from    TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    valid_to      TIMESTAMPTZ,
    CONSTRAINT pk_init_permissions PRIMARY KEY (id)
);

-- ****************************************
-- ************ Foreign Keys **************
-- ****************************************

-- diagnostics.init → initiatives.id
ALTER TABLE diagnostics
    ADD CONSTRAINT fk_diagnostics_initiatives
    FOREIGN KEY (init)
    REFERENCES initiatives (id);

-- diagnostics.criteria → criterias.code
ALTER TABLE diagnostics
    ADD CONSTRAINT fk_diagnostics_criterias
    FOREIGN KEY (criteria)
    REFERENCES criterias (code);

-- favorite_inits.user_id → users.id
ALTER TABLE favorite_inits
    ADD CONSTRAINT fk_favorite_inits_users
    FOREIGN KEY (user_id)
    REFERENCES users (id);

-- favorite_inits.init → initiatives.id
ALTER TABLE favorite_inits
    ADD CONSTRAINT fk_favorite_inits_initiatives
    FOREIGN KEY (init)
    REFERENCES initiatives (id);

-- init_permissions.init → initiatives.id
ALTER TABLE init_permissions
    ADD CONSTRAINT fk_init_permissions_initiatives
    FOREIGN KEY (init)
    REFERENCES initiatives (id);

-- collaborations.init → initiatives.id
ALTER TABLE collaborations 
    ADD CONSTRAINT fk_collaborations_initiatives
    FOREIGN KEY (init)
    REFERENCES initiatives (id);

-- collaborations.user_id → users.id
ALTER TABLE collaborations
    ADD CONSTRAINT fk_collaborations_users
    FOREIGN KEY (user_id)
    REFERENCES users (id);

-- collaborations.parent → collaborations.id
ALTER TABLE collaborations
    ADD CONSTRAINT fk_collaborations_collaborations
    FOREIGN KEY (parent)
    REFERENCES collaborations(id);

-- related_inits.source → initiatives.id
ALTER TABLE related_inits 
    ADD CONSTRAINT fk_related_inits_source
    FOREIGN KEY (source)
    REFERENCES initiatives (id);

-- related_inits.target → initiatives.id
ALTER TABLE related_inits 
    ADD CONSTRAINT fk_related_inits_target
    FOREIGN KEY (target)
    REFERENCES initiatives (id);

